"""VNBOT Domain — Reminder entity and value objects.

Reminder ≠ Occurrence. Recurring reminders are RULES, not duplicated rows.
The scheduler generates occurrences with a 30-day lookahead window.

Key invariant: occurrence_key is deterministic {reminder_id}:{due_at_utc}:{rule_version}.
This guarantees idempotent delivery: the same occurrence fires exactly once.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from datetime import datetime, timedelta, timezone
from enum import Enum
from typing import Literal
from uuid import UUID, uuid4

from .common import utc_now, Authority, Provenance, Sensitivity


