@dataclass
class ReminderOccurrence:
    """Concrete instance of a reminder at a specific time (per docs/07).

    occurrence_key is DETERMINISTIC: {reminder_id}:{due_at_utc_iso}:{rule_version}.
    This is the idempotency key for delivery — same key means same occurrence,
    so duplicate deliveries are detected.
    """

    id: UUID
    reminder_id: UUID
    workspace_id: UUID
    occurrence_key: str
    due_at: datetime
    timezone: str = "UTC"
    status: OccurrenceStatus = OccurrenceStatus.PENDING
    attempts: int = 0
    delivered_at: datetime | None = None
    last_error_code: str | None = None
    created_at: datetime = field(default_factory=utc_now)
    updated_at: datetime = field(default_factory=utc_now)

    @classmethod
    def generate_key(cls, reminder_id: UUID, due_at: datetime, rule_version: int = 1) -> str:
        """Compute the deterministic occurrence_key.

        This is the idempotency guarantee: same reminder + same time + same rule
        always produces the same key, so duplicate scheduler ticks don't create
        duplicate occurrences.
        """
        due_iso = due_at.astimezone(timezone.utc).strftime("%Y%m%dT%H%M%SZ")
        return f"{reminder_id}:{due_iso}:v{rule_version}"

    def mark_delivered(self) -> None:
        """Mark this occurrence as delivered."""
        if self.status in (OccurrenceStatus.DELIVERED, OccurrenceStatus.COMPLETED):
            raise ValueError(f"Occurrence already in terminal status {self.status}")
        self.status = OccurrenceStatus.DELIVERED
        self.delivered_at = utc_now()
        self.updated_at = utc_now()

    def mark_failed(self, error_code: str) -> None:
        """Mark this occurrence as failed (after retries exhausted)."""
        self.status = OccurrenceStatus.FAILED
        self.last_error_code = error_code
        self.attempts += 1
        self.updated_at = utc_now()

    def mark_snoozed(self) -> None:
        """Mark as snoozed (will be rescheduled)."""
        self.status = OccurrenceStatus.SNOOZED
        self.updated_at = utc_now()

    def mark_completed(self) -> None:
        """Mark as completed (user confirmed)."""
        self.status = OccurrenceStatus.COMPLETED
        self.updated_at = utc_now()

    def is_past_due(self) -> bool:
        """Check if this occurrence is past its due time."""
        return utc_now() >= self.due_at
