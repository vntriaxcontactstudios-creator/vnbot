@dataclass
class Reminder:
    """Reminder entity (per docs/07 §1).

    A Reminder is the user's intent to be notified. Concrete instances are
    ReminderOccurrence rows generated by the scheduler.

    Invariants:
    - next_due_at must be in the future when status=ACTIVE
    - recurrence_rule must be present if status=ACTIVE and frequency != NONE
    - timezone must be a valid IANA timezone string
    """

    id: UUID
    workspace_id: UUID
    created_by_user_id: UUID
    title: str
    description: str | None = None
    timezone: str = "UTC"
    recurrence_rule: RecurrenceRule = field(default_factory=RecurrenceRule)
    priority: ReminderPriority = ReminderPriority.NORMAL
    channel: ReminderChannel = ReminderChannel.MOCK
    recipient_ref: str | None = None
    status: ReminderStatus = ReminderStatus.ACTIVE
    source_memory_id: UUID | None = None
    next_due_at: datetime | None = None
    provenance: Provenance = Provenance.EXPLICIT_USER_INPUT
    authority: Authority = Authority.EXPLICIT
    sensitivity: Sensitivity = Sensitivity.PERSONAL
    created_at: datetime = field(default_factory=utc_now)
    updated_at: datetime = field(default_factory=utc_now)
    completed_at: datetime | None = None
    cancelled_at: datetime | None = None
    # Sync fields (present from day 1 per ADR-0005, used for audit/export even without sync)
    version: int = 1
    device_id: str | None = None

    def __post_init__(self) -> None:
        """Validate invariants."""
        if not self.title or not self.title.strip():
            raise ValueError("Reminder title cannot be empty")
        if self.status == ReminderStatus.ACTIVE and self.next_due_at is None:
            # Allow None for newly created reminders before scheduler computes it
            pass

    def complete(self) -> None:
        """Mark the reminder as completed."""
        if self.status not in (ReminderStatus.ACTIVE, ReminderStatus.PAUSED):
            raise ValueError(f"Cannot complete reminder in status {self.status}")
        self.status = ReminderStatus.COMPLETED
        self.completed_at = utc_now()
        self.updated_at = utc_now()
        self.version += 1

    def cancel(self) -> None:
        """Cancel the reminder."""
        if self.status in (ReminderStatus.COMPLETED, ReminderStatus.CANCELLED):
            raise ValueError(f"Cannot cancel reminder in status {self.status}")
        self.status = ReminderStatus.CANCELLED
        self.cancelled_at = utc_now()
        self.updated_at = utc_now()
        self.version += 1

    def pause(self) -> None:
        """Pause the reminder (no new occurrences generated while paused)."""
        if self.status != ReminderStatus.ACTIVE:
            raise ValueError(f"Cannot pause reminder in status {self.status}")
        self.status = ReminderStatus.PAUSED
        self.updated_at = utc_now()
        self.version += 1

    def resume(self) -> None:
        """Resume a paused reminder."""
        if self.status != ReminderStatus.PAUSED:
            raise ValueError(f"Cannot resume reminder in status {self.status}")
        self.status = ReminderStatus.ACTIVE
        self.updated_at = utc_now()
        self.version += 1

    def snooze(self, until: datetime) -> None:
        """Snooze the reminder until a future time."""
        if self.status != ReminderStatus.ACTIVE:
            raise ValueError(f"Cannot snooze reminder in status {self.status}")
        if until <= utc_now():
            raise ValueError("Snooze time must be in the future")
        self.next_due_at = until
        self.updated_at = utc_now()
        self.version += 1


